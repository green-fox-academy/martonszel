'use strict';

// Create a program that prints a few operations on two numbers: 22 and 13

let a = 13 ;
let b = 22 ; 
console.log(a+b); // Print the result of 13 added to 22
console.log(b-a); // Print the result of 13 substracted from 22
console.log(a*b); // Print the result of 22 multiplied by 13
console.log(b/a); // Print the result of 22 divided by 13 (as a decimal fraction)
console.log(b%a); // Print the reminder of 22 divided by 13